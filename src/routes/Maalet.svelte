<h1>Bærekrafts mål 12: Ansvarlig forbruk og produksjon</h1>

<p>Bærekraftig forbruk og produksjon handler om å fremme ressurs- og energieffektivitet,
bærekraftig infrastruktur og gi tilgang til grunnleggende
tjenester, grønne og anstendige arbeidsplasser og en bedre livskvalitet for
alle. Gjennomføringen bidrar til å oppnå overordnede utviklingsplaner, redusere
fremtidige økonomiske, miljømessige og sosiale kostnader, styrke
økonomisk konkurranseevne og redusere fattigdom.
På nåværende tidspunkt øker materialforbruket av naturressurser, spesielt
i Øst-Asia. Land fortsetter også å takle utfordringer knyttet til luft, vann og
jordforurensning.
Siden bærekraftig forbruk og produksjon tar sikte på å “gjøre mer og bedre
med mindre”, kan nettomsorgsgevinstene fra økonomiske aktiviteter øke
ved å redusere ressursbruk, nedbrytning og forurensning gjennom hele
livssyklusen, samtidig som livskvaliteten økes. Det må også være betydelig
fokus på drift på forsyningskjeden, som involverer alle fra produsent
til sluttbrukere. Dette inkluderer utdannelse av forbrukere om bærekraftig
forbruk og livsstil, og gir dem tilstrekkelig informasjon gjennom standarder
og etiketter og engasjere seg i bærekraftig offentlig anskaffelse, blant
annet.</p>
<br/><br/>

<h2>Delmål</h2>
<p><b>12.1</b><br/>
Gjennomføre det tiårige handlingsprogrammet for bærekraftig forbruk og
produksjon, med deltakelse fra alle land og der de utviklede landene går
foran, samtidig som det tas hensyn til utviklingslandenes utviklingsnivå og
muligheter.<br/><br/>
<b>12.2</b><br/>
Innen 2030 oppnå en bærekraftig forvaltning og effektiv bruk av naturressurser.<br/>
<b>12.3</b><br/>
Innen 2030 halvere andelen matavfall per innbygger på verdensbasis,
både i detaljhandelen og blant forbrukere, og redusere svinn i produksjons-
og
forsyningskjeden, herunder svinn som følge av dårlig lagerkapasitet etter
innhøsting.<br/><br/>
<b>12.4</b><br/>
Innen 2020, og i samsvar med internasjonalt vedtatte rammeverk, oppnå
en mer miljøvennlig forvaltning av kjemikalier og alle former for avfall gjennom
hele deres livssyklus, og betydelig redusere utslipp av kjemikalier og
avfall til luft, vann og jord for mest mulig å begrense skadevirkningene for
menneskers helse og for miljøet.<br/><br/>
<b>12.5</b><br/>
Innen 2030 betydelig redusere avfallsmengden gjennom forbud, reduksjon,
gjenvinning og ombruk.<br/><br/>
<b>12.6</b><br/>
Stimulere selskaper, særlig store og flernasjonale selskaper, til å innføre
bærekraftige arbeidsmetoder og integrere informasjon om bærekraft i
sine
rapporteringsrutiner.<br/><br/>
<b>12.7</b><br/>
Fremme bærekraftige ordninger for offentlige anskaffelser, i samsvar med
de enkelte landenes politikk og prioriteringer.<br/><br/>
<b>12.8</b><br/>
Innen 2030 sikre at alle i hele verden har relevant informasjon om og er
seg bevisst en bærekraftig utvikling og en livsstil som er i harmoni med
naturen.<br/><br/>
<b>12.a</b><br/>
Støtte utviklingslandene i arbeidet med å styrke deres vitenskapelige og
tekniske kapasitet til gradvis å innføre mer bærekraftige forbruks- og produksjonsmønstre.<br/><br/>
<b>12.b</b><br/>
Utvikle og innføre metoder for å overvåke konsekvensene av en bærekraftig
utvikling på en turistnæring som er bærekraftig, skaper arbeidsplasser
og fremmer lokal kultur og lokale produkter.<br/><br/>
<b>12.c</b><br/>
Redusere ineffektive subsidier til fossilt brensel ved å fjerne markedsvridninger
som oppmuntrer til overforbruk, i samsvar med forholdene i de enkelte
land, herunder ved å legge om skatter og avgifter og avvikle skadelige
subsidier der de finnes, slik at konsekvensene for miljøet avdekkes,
samtidig som det tas fullt ut hensyn til utviklingslandenes særlige behov
og situasjon og eventuelle skadelige konsekvenser for deres utvikling begrenses
mest mulig og på en måte som beskytter de fattige og de berørte
lokalsamfunnene.</p>

